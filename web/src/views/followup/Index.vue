    default: '您好，我是东芳美诊所的客服，想了解一下您治疗后的恢复情况，有什么需要帮助的吗？'
  },
  dormant: { title: '沉睡唤醒话术', text: '您好，我是东芳美诊所的客服。好久不见，最近我们有一些老客户回馈活动，想邀请您来店体验一下。您方便的时候可以预约哦。' },
  birthday: { title: '生日关怀话术', text: '您好，我是东芳美诊所的客服。今天是您的生日，祝您生日快乐！我们为您准备了一份生日专属礼物，欢迎您来店领取。祝您越来越年轻美丽！' },
  custom: { title: '自定义回访', text: '根据实际情况进行回访沟通。' }
}
function calcDay(due: string): number | null {
  if (!due) return null
  // Try to calculate D-day by looking at the day relative to something
  // For display, just show the last 2 digits
  return null
}
function scriptHintByType(type: string): string {
  const h = SCRIPT_HINTS[type]
  if (!h) return ''
  return h.title + '：' + (h.text || h.default || '')
}
function scriptHint(row: any): string {
  if (!row) return ''
  const h = SCRIPT_HINTS[row.type]
  if (!h) return '无预设话术'
  if (row.type === 'post_treatment' && row.note) {
    // Try to detect D1/D3/D7/D30 from the note
    if (row.note.includes('第1天') || row.note.includes('D1')) return h.d1
    if (row.note.includes('第3天') || row.note.includes('D3')) return h.d3
    if (row.note.includes('第7天') || row.note.includes('D7')) return h.d7
    if (row.note.includes('第30天') || row.note.includes('D30')) return h.d30
  }
  return h.text || h.default || h.title || ''
}

// Stats computed from tasks
const stats = computed(() => {
  const a = tasks.value
  const now = Date.now()
  const pending = a.filter(x => x.status === 'pending').length
  const completed = a.filter(x => x.status === 'completed').length
  const overdue = a.filter(x => x.status === 'pending' && x.due_date && new Date(x.due_date).getTime() < now).length
  const rate = a.length > 0 ? Math.round(completed / a.length * 100) : 0
  const rebooked = a.filter(x => x.result_status === 'rebooked').length
  return { total: a.length, pending, completed, overdue, rate, rebooked }
})

// Breakdown stats
const statsBreakdown = computed(() => {
  const map: Record<string, any> = {}
  tasks.value.forEach((x: any) => {
    const t = x.type || 'undefined'
    if (!map[t]) map[t] = { type: t, total: 0, completed: 0, rebooked: 0, rate: 0, conv_rate: 0 }
    map[t].total++
    if (x.status === 'completed') map[t].completed++
    if (x.result_status === 'rebooked') map[t].rebooked++
  })
  return Object.values(map).map(r => ({
    ...r,
    rate: r.total > 0 ? Math.round(r.completed / r.total * 100) : 0,
    conv_rate: r.completed > 0 ? Math.round(r.rebooked / r.completed * 100) : 0
  }))
})

// Conversion stats from API
const convStats = reactive({ total: 0, completed: 0, rebooked: 0, rate: '0%' })
async function loadConvStats() {
  try {
    const r = await api.get('/followup/tasks/stats')
    const d = r.data
    if (d?.overall) {
      convStats.total = d.overall.total || 0
      convStats.completed = d.overall.completed || 0
      convStats.rebooked = d.overall.rebooked || 0
      convStats.rate = (d.overall.rate || 0).toFixed(1) + '%'
    }
  } catch { }
}

// Calendar helper
function tasksByDate(date: string) {
  return tasks.value.filter(t => t.due_date === date)
}

// Staff list
const staffList = ref<any[]>([])
async function loadStaff() {
  try {
    const r = await api.get('/users')
    staffList.value = Array.isArray(r.data) ? r.data.filter((u: any) => u.status === 'active') : []
  } catch { staffList.value = [] }
}
function userName(id: number | null): string {
  if (!id) return ''
  const s = staffList.value.find(u => u.id === id)
  return s?.real_name || ''
}

// Load tasks
async function loadTasks() {
  loading.value = true
  try {
    const params: any = {}
    if (statusFilter.value) params.status = statusFilter.value
    if (typeTab.value) params.type = typeTab.value
    if (assignedFilter.value) params.assigned_to = assignedFilter.value
    const r = await api.get('/followup/tasks', { params })
    tasks.value = Array.isArray(r.data) ? r.data : []
  } catch { tasks.value = [] }
  finally { loading.value = false }
}

// Create dialog
const createVisible = ref(false)
const saving = ref(false)
const custOptions = ref<any[]>([])
const custLoading = ref(false)
const createForm = ref({ customer_id: null, type: 'custom', due_date: '', assigned_to: null, note: '' })

async function searchCust(q: string) {
  if (!q) { custOptions.value = []; return }
  custLoading.value = true
  try {
    const r = await api.get('/customers', { params: { q, page_size: 20 } })
    custOptions.value = Array.isArray(r.data) ? r.data : (r.data?.data || [])
  } catch { custOptions.value = [] }
  finally { custLoading.value = false }
}

function openCreate() {
  createForm.value = { customer_id: null, type: 'custom', due_date: new Date().toISOString().slice(0, 10), assigned_to: null, note: '' }
  createVisible.value = true
}

async function submitCreate() {
  if (!createForm.value.customer_id) { ElMessage.warning('请选择客户'); return }
  if (!createForm.value.due_date) { ElMessage.warning('请选择截止日期'); return }
  saving.value = true
  try {
    await api.post('/followup/tasks', createForm.value)
    ElMessage.success('创建成功')
    createVisible.value = false
    await loadTasks()
  } catch (e: any) { ElMessage.error(e?.response?.data?.error || '创建失败') }
  finally { saving.value = false }
}

// Complete dialog
const completeVisible = ref(false)
const completing = ref<any>(null)
const completeForm = ref({ result: '', result_status: '' })

function openComplete(row: any) {
  completing.value = row
  completeForm.value = { result: '', result_status: '' }
  completeVisible.value = true
}

async function submitComplete() {
  if (!completing.value?.id) return
  saving.value = true
  try {
    await api.put('/followup/tasks/' + completing.value.id + '/complete', completeForm.value)
    ElMessage.success('已完成')
    completeVisible.value = false
    completing.value = null
    await loadTasks()
    await loadConvStats()
  } catch (e: any) { ElMessage.error(e?.response?.data?.error || '操作失败') }
  finally { saving.value = false }
}

// Auto-generate
async function autoGenerateDormant() {
  try {
    const r = await api.post('/followup/tasks/auto-generate')
    ElMessage.success(r.data?.message || '已生成')
    await loadTasks()
  } catch (e: any) { ElMessage.error(e?.response?.data?.error || '生成失败') }
}

async function autoGeneratePostTreatment() {
  try {
    const r = await api.post('/followup/tasks/auto-generate-post-treatment', { customer_id: 0, treat_date: '' })
    ElMessage.success(r.data?.message || '已生成')
    await loadTasks()
  } catch { }
}

// Export CSV
function exportCSV() {
  const a = tasks.value; if (!a.length) return
  const sep = ',', nl = '\n'
  const h = '客户' + sep + '电话' + sep + '类型' + sep + '截止日期' + sep + '状态' + sep + '负责人' + sep + '回访结果' + sep + '沟通记录' + sep + '备注'
  const r = a.map(function (x: any) {
    return [x.customer?.name || '', x.customer?.phone || '', typeLabel(x.type), x.due_date || '',
      x.status === 'completed' ? '已回访' : '待回访', userName(x.assigned_to), resultLabel(x.result_status),
      x.result || '', x.note || ''].join(sep)
  }).join(nl)
  const blob = new Blob(['\ufeff' + h + nl + r], { type: 'text/csv;charset=utf-8' })
  const el = document.createElement('a')
  el.href = URL.createObjectURL(blob)
  el.download = '回访任务_' + new Date().toISOString().slice(0, 10) + '.csv'
  el.click()
  URL.revokeObjectURL(el.href)
}

