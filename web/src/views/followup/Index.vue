<script setup lang="ts">
import { ref, reactive, computed, onMounted } from 'vue'
import { ElMessage, ElMessageBox } from 'element-plus'
import { InfoFilled } from '@element-plus/icons-vue'
import api from '../../api'

const loading = ref(false)
const tasks = ref<any[]>([])
const statusFilter = ref('')
const typeTab = ref('')
const assignedFilter = ref('')
const viewTab = ref('list')
const calendarDate = ref(new Date())

// Type labels
const typeL: Record<string, string> = {
  post_treatment: '治疗后回访', dormant: '沉睡唤醒',
  birthday: '生日关怀', custom: '自定义'
}
function typeLabel(s: string) { return typeL[s] || s || '未知' }

// Result labels
const resultL: Record<string, string> = {
  satisfied: '恢复良好', rebooked: '已预约复购',
  discomfort: '有不适', no_answer: '未接通',
  complaint: '投诉', other: '其他'
}
function resultLabel(s: string) { return resultL[s] || s || '--' }

// Script hints
const SCRIPT_HINTS: Record<string, Record<string, string>> = {
  post_treatment: {
    title: '治疗后回访话术',
    d1: '您好，我是东芳美诊所的客服。昨天做完治疗后，您感觉怎么样？有没有出现红肿、疼痛等不适？请您注意保持治疗部位清洁，避免暴晒和剧烈运动。',
